def run_benchmark(*, layers: set[str], n_per_layer: int | None,
                   llm_call: Callable[[str], str] | None) -> dict:
    expire_cases = EXPIRE_CASES if n_per_layer is None else EXPIRE_CASES[:n_per_layer]
    archive_cases = ARCHIVE_CASES if n_per_layer is None else ARCHIVE_CASES[:n_per_layer]
    consolidate_cases = (CONSOLIDATE_CASES if n_per_layer is None
                          else CONSOLIDATE_CASES[:n_per_layer])
    entity_cases = ENTITY_CASES if n_per_layer is None else ENTITY_CASES[:n_per_layer]

    report: dict = {
        "schema_version": 1,
        "generated_at": datetime.now(timezone.utc).isoformat(timespec="seconds"),
        "n_per_layer": n_per_layer,
        "llm_used": llm_call is not None,
    }

    if "A" in layers:
        a = run_layer_a(expire_cases, archive_cases, llm_call=llm_call)
        report["layer_a"] = {
            "description": "lifecycle_decisions: should_expire / should_archive",
            "sections": a,
        }
    if "B" in layers:
        b = run_layer_b(consolidate_cases, llm_call=llm_call)
        report["layer_b"] = {
            "description": "memory_engine._heuristic_decide_memory_action vs curator_consolidate",
            "sections": {"consolidate": b},
        }
    if "C" in layers:
        c = run_layer_c(entity_cases, llm_call=llm_call)
        report["layer_c"] = {
            "description": "graph._extract_entities_heuristic vs curator_extract_entities",
            "sections": {"entities": c},
        }

    report["recommendation"] = _recommendation(report)
    return report


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--layer", choices=("A", "B", "C", "all"), default="all")
    parser.add_argument("--smoke", action="store_true",
                        help="10 cases per layer instead of 30")
    parser.add_argument("--full", action="store_true",
                        help="Use the entire hand-graded set per layer")
    parser.add_argument("--no-llm", action="store_true",
                        help="Skip the LLM side; run heuristic only")
    parser.add_argument("--openai", action="store_true",
                        help="Use OpenAI as LLM judge (requires OPENAI_API_KEY)")
    parser.add_argument("--openai-model", default="gpt-4o-mini")
    parser.add_argument("--out", default=".memoirs/heuristic_vs_llm_quality.json")
    parser.add_argument("--quiet", action="store_true")
    args = parser.parse_args(argv)

    logging.basicConfig(
        level=logging.WARNING if args.quiet else logging.INFO,
        format="%(asctime)s %(levelname)s %(message)s",
    )

    if args.smoke:
        n = 10
    elif args.full:
        n = None
    else:
        n = 30

    layers = {"A", "B", "C"} if args.layer == "all" else {args.layer}

    llm_call = None
    if not args.no_llm:
        if args.openai:
            llm_call = _build_openai_caller(args.openai_model)
            if llm_call is None:
                log.warning("OpenAI requested but unavailable; running heuristic only")
        else:
            llm_call = _build_qwen_caller()
            if llm_call is None and os.environ.get("OPENAI_API_KEY"):
                log.info("Qwen unavailable; falling back to OpenAI")
                llm_call = _build_openai_caller(args.openai_model)
            if llm_call is None:
                log.warning("No LLM available; running heuristic only")

    report = run_benchmark(layers=layers, n_per_layer=n, llm_call=llm_call)

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(report, indent=2))
    if not args.quiet:
        _print_table(report)
        print(f"\nReport written to {out_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
