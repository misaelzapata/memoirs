@dataclass
class BinaryStats:
    """Stats for a yes/no decision against ground truth."""
    tp: int = 0
    tn: int = 0
    fp: int = 0
    fn: int = 0

    @property
    def total(self) -> int:
        return self.tp + self.tn + self.fp + self.fn

    @property
    def accuracy(self) -> float:
        n = self.total
        return (self.tp + self.tn) / n if n else 0.0

    @property
    def fpr(self) -> float:
        d = self.fp + self.tn
        return self.fp / d if d else 0.0

    @property
    def fnr(self) -> float:
        d = self.fn + self.tp
        return self.fn / d if d else 0.0


def _record(stats: BinaryStats, predicted: bool, truth: bool) -> None:
    if predicted and truth:
        stats.tp += 1
    elif not predicted and not truth:
        stats.tn += 1
    elif predicted and not truth:
        stats.fp += 1
    else:
        stats.fn += 1


def _percentile(values: list[float], pct: float) -> float:
    if not values:
        return 0.0
    s = sorted(values)
    k = max(0, min(len(s) - 1, int(round((pct / 100.0) * (len(s) - 1)))))
    return float(s[k])


def _summary(latencies_ms: list[float]) -> dict:
    if not latencies_ms:
        return {"n": 0, "mean_ms": 0.0, "p50_ms": 0.0, "p95_ms": 0.0}
    return {
        "n": len(latencies_ms),
        "mean_ms": round(statistics.mean(latencies_ms), 2),
        "p50_ms": round(_percentile(latencies_ms, 50), 2),
        "p95_ms": round(_percentile(latencies_ms, 95), 2),
    }


