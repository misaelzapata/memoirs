def _build_qwen_caller():
    """Load the local Qwen GGUF and return a `lambda prompt: text` callable.

    Returns None if llama_cpp / weights are missing.
    """
    try:
        from memoirs.engine import curator as _c
    except Exception:
        return None
    if not _c._have_curator():
        return None
    try:
        llm = _c._get_llm()
    except Exception as e:
        log.warning("qwen load failed: %s", e)
        return None

    def _call(prompt: str) -> str:
        body = _c._chat_user_turn(prompt)
        try:
            out = llm.create_completion(
                prompt=body,
                max_tokens=80,
                temperature=0.1,
                stop=_c._chat_stops(),
            )
        except Exception as e:
            log.warning("qwen call raised: %s", e)
            return ""
        return (out.get("choices", [{}])[0].get("text") or "").strip()

    return _call


def _build_openai_caller(model: str = "gpt-4o-mini"):
    api_key = os.environ.get("OPENAI_API_KEY")
    if not api_key:
        return None
    try:
        from openai import OpenAI
    except Exception:
        log.warning("openai SDK not installed")
        return None
    client = OpenAI(api_key=api_key)

    def _call(prompt: str) -> str:
        try:
            resp = client.chat.completions.create(
                model=model,
                messages=[{"role": "user", "content": prompt}],
                temperature=0.1,
                max_tokens=80,
            )
            return (resp.choices[0].message.content or "").strip()
        except Exception as e:
            log.warning("openai call raised: %s", e)
            return ""

    return _call


