def _print_table(report: dict) -> None:
    print()
    print("=" * 78)
    print("A/B benchmark: heuristic vs LLM curator")
    print("=" * 78)
    for layer in ("layer_a", "layer_b", "layer_c"):
        sub = report.get(layer)
        if not sub:
            continue
        print(f"\n[{layer.upper()}] {sub.get('description', '')}")
        for sec_name, sec in sub.get("sections", {}).items():
            print(f"  · {sec_name} (n={sec['n']})")
            h = sec["heuristic"]
            l = sec.get("llm")
            print(
                f"    heuristic: " + ", ".join(
                    f"{k}={v}" for k, v in h.items() if k != "latency"
                )[:200]
            )
            print(f"      latency: {h['latency']}")
            if l is not None:
                print(
                    f"    llm:       " + ", ".join(
                        f"{k}={v}" for k, v in l.items() if k != "latency"
                    )[:200]
                )
                print(f"      latency: {l['latency']}")
                if "agreement_rate" in sec and sec["agreement_rate"] is not None:
                    print(f"    agreement: {sec['agreement_rate']}")
                if "agreement_jaccard_mean" in sec and sec.get("agreement_jaccard_mean") is not None:
                    print(f"    jaccard:   {sec['agreement_jaccard_mean']}")
    rec = report.get("recommendation")
    if rec:
        print("\nRecommendation:")
        for line in rec.splitlines():
            print(f"  {line}")
    print("=" * 78)


def _recommendation(report: dict) -> str:
    """Crude rule-of-thumb verdict per layer based on accuracy delta."""
    lines: list[str] = []
    a = report.get("layer_a", {}).get("sections", {})
    b = report.get("layer_b", {}).get("sections", {})
    c = report.get("layer_c", {}).get("sections", {})

    def _verdict(h_acc: float | None, l_acc: float | None,
                  h_p95: float, l_p95: float, label: str) -> str:
        if l_acc is None or h_acc is None:
            return f"{label}: heuristic only (LLM not available)"
        delta = l_acc - h_acc
        slowdown = (l_p95 / h_p95) if h_p95 > 0 else float("inf")
        if delta > 0.10:
            return (f"{label}: LLM wins (+{delta:.2f} acc, {slowdown:.0f}x slower) "
                    "→ keep heuristic default + LLM opt-in for high-stakes paths")
        if delta < -0.05:
            return (f"{label}: heuristic wins ({-delta:.2f} better, "
                    f"{slowdown:.0f}x faster) → keep heuristic")
        return (f"{label}: tie (Δacc={delta:+.2f}); heuristic is "
                f"{slowdown:.0f}x faster → keep heuristic")

    if a:
        for k in ("expire", "archive"):
            sec = a.get(k)
            if not sec:
                continue
            h = sec["heuristic"]
            l = sec.get("llm")
            lines.append(_verdict(
                h.get("accuracy"),
                (l or {}).get("accuracy"),
                h.get("latency", {}).get("p95_ms", 0),
                (l or {}).get("latency", {}).get("p95_ms", 0),
                f"A.{k}",
            ))
    if b:
        sec = b.get("consolidate")
        if sec:
            h = sec["heuristic"]; l = sec.get("llm")
            lines.append(_verdict(
                h.get("accuracy"),
                (l or {}).get("accuracy"),
                h.get("latency", {}).get("p95_ms", 0),
                (l or {}).get("latency", {}).get("p95_ms", 0),
                "B.consolidate",
            ))
    if c:
        sec = c.get("entities")
        if sec:
            h = sec["heuristic"]; l = sec.get("llm")
            lines.append(_verdict(
                h.get("f1_mean"),
                (l or {}).get("f1_mean"),
                h.get("latency", {}).get("p95_ms", 0),
                (l or {}).get("latency", {}).get("p95_ms", 0),
                "C.entities",
            ))
    if not lines:
        lines.append("no layers run")
    return "\n".join(lines)


