def _ent_metrics(predicted: set[str], truth: set[str]) -> dict:
    if not predicted and not truth:
        return {"precision": 1.0, "recall": 1.0, "f1": 1.0,
                "tp": 0, "fp": 0, "fn": 0}
    tp = len(predicted & truth)
    fp = len(predicted - truth)
    fn = len(truth - predicted)
    precision = tp / (tp + fp) if (tp + fp) else (1.0 if not truth else 0.0)
    recall = tp / (tp + fn) if (tp + fn) else 1.0
    f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) else 0.0
    return {"precision": round(precision, 3), "recall": round(recall, 3),
            "f1": round(f1, 3), "tp": tp, "fp": fp, "fn": fn}


def _llm_judge_entities(text: str,
                         *, llm_call: Callable[[str], str]) -> set[str]:
    prompt = (
        "Extract named entities (tools, projects, technologies, people). "
        "Output ONLY a comma-separated list of names, lowercased. No prose.\n"
        f"Text: {text}\n"
        "Entities:"
    )
    raw = llm_call(prompt)
    line = raw.strip().splitlines()[0].strip() if raw.strip() else ""
    out = {p.strip().lower() for p in line.split(",") if p.strip()}
    out = {n for n in out if 2 <= len(n) <= 40}
    return out


def run_layer_c(cases: list, *, llm_call: Callable[[str], str] | None) -> dict:
    from memoirs.engine.graph import _extract_entities_heuristic

    h_p: list[float] = []
    h_r: list[float] = []
    h_f: list[float] = []
    l_p: list[float] = []
    l_r: list[float] = []
    l_f: list[float] = []
    h_lat: list[float] = []
    l_lat: list[float] = []
    agree_per_case: list[float] = []
    examples: list[dict] = []

    for (text, expected) in cases:
        t0 = time.perf_counter()
        h_ents = {n.lower() for (n, _t) in _extract_entities_heuristic(text)}
        h_lat.append((time.perf_counter() - t0) * 1000)
        h_m = _ent_metrics(h_ents, expected)
        h_p.append(h_m["precision"])
        h_r.append(h_m["recall"])
        h_f.append(h_m["f1"])

        if llm_call is not None:
            t0 = time.perf_counter()
            l_ents = _llm_judge_entities(text, llm_call=llm_call)
            l_lat.append((time.perf_counter() - t0) * 1000)
            l_m = _ent_metrics(l_ents, expected)
            l_p.append(l_m["precision"])
            l_r.append(l_m["recall"])
            l_f.append(l_m["f1"])
            inter = h_ents & l_ents
            union = h_ents | l_ents
            agree_per_case.append(len(inter) / len(union) if union else 1.0)
        else:
            l_ents = None

        if len(examples) < 10:
            examples.append({
                "text": text[:80],
                "expected": sorted(expected),
                "heuristic": sorted(h_ents),
                "llm": sorted(l_ents) if l_ents is not None else None,
                "h_f1": h_m["f1"],
            })

    def _avg(xs: list[float]) -> float:
        return round(sum(xs) / len(xs), 3) if xs else 0.0

    return {
        "n": len(cases),
        "heuristic": {
            "precision_mean": _avg(h_p),
            "recall_mean": _avg(h_r),
            "f1_mean": _avg(h_f),
            "latency": _summary(h_lat),
        },
        "llm": (
            {"precision_mean": _avg(l_p),
             "recall_mean": _avg(l_r),
             "f1_mean": _avg(l_f),
             "latency": _summary(l_lat)}
            if llm_call is not None else None
        ),
        "agreement_jaccard_mean": (
            round(sum(agree_per_case) / len(agree_per_case), 3)
            if llm_call is not None and agree_per_case else None
        ),
        "examples": examples,
    }


