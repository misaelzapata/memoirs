def _make_neighbor(content: str, ntype: str, age_days: int, sim: float,
                   *, usage: int = 0, score: float = 0.5) -> dict:
    created = datetime.now(timezone.utc) - timedelta(days=age_days)
    return {
        "id": f"mem-{abs(hash((content, ntype, age_days)))%10**8}",
        "type": ntype,
        "content": content,
        "created_at": created.isoformat(),
        "similarity": sim,
        "usage_count": usage,
        "score": score,
    }


def _llm_judge_expire(cand_content: str, neighbor_content: str,
                       *, llm_call: Callable[[str], str]) -> tuple[bool, str]:
    """Ask the LLM whether the neighbor is obsolete given the candidate.

    Returns (decision, raw_text). Falls back to False on parse failure.
    """
    prompt = (
        "Two statements about the same user. Decide if statement B is now "
        "OBSOLETE because statement A supersedes it (e.g. user moved, "
        "changed job, swapped technologies). Reply with ONLY one of:\n"
        "  YES\n"
        "  NO\n"
        f"A: {cand_content}\n"
        f"B: {neighbor_content}\n"
        "Answer:"
    )
    raw = llm_call(prompt)
    head = raw.strip().splitlines()[0].strip().upper() if raw.strip() else ""
    if head.startswith("YES"):
        return True, raw
    if head.startswith("NO"):
        return False, raw
    return False, raw


def _llm_judge_archive(content: str, age_days: int, usage: int, score: float, mtype: str,
                        *, llm_call: Callable[[str], str]) -> tuple[bool, str]:
    prompt = (
        "Decide if a stored memory should be ARCHIVED (no longer surfaced) "
        "given its metadata. Durable user preferences and important facts "
        "should NOT be archived even if dusty. Reply with ONLY YES or NO.\n"
        f"content: {content}\n"
        f"type: {mtype}\n"
        f"age_days: {age_days}\n"
        f"usage_count: {usage}\n"
        f"score: {score}\n"
        "Answer:"
    )
    raw = llm_call(prompt)
    head = raw.strip().splitlines()[0].strip().upper() if raw.strip() else ""
    if head.startswith("YES"):
        return True, raw
    if head.startswith("NO"):
        return False, raw
    return False, raw


def run_layer_a(cases_expire: list, cases_archive: list,
                *, llm_call: Callable[[str], str] | None) -> dict:
    """Run EXPIRE + ARCHIVE A/B."""
    from memoirs.engine.lifecycle_decisions import should_expire, should_archive
    from memoirs.engine.curator import Candidate

    out: dict = {}

    # ---- EXPIRE ----
    h_stats = BinaryStats()
    l_stats = BinaryStats()
    agree = 0
    h_lat: list[float] = []
    l_lat: list[float] = []
    examples: list[dict] = []

    for (cc, ct, nc, nt, age, sim, gt) in cases_expire:
        cand = Candidate(type=ct, content=cc)
        nb = _make_neighbor(nc, nt, age, sim)
        t0 = time.perf_counter()
        h_pred, h_reason = should_expire(cand, nb, use_gemma=False)
        h_lat.append((time.perf_counter() - t0) * 1000)

        if llm_call is not None:
            t0 = time.perf_counter()
            l_pred, _l_raw = _llm_judge_expire(cc, nc, llm_call=llm_call)
            l_lat.append((time.perf_counter() - t0) * 1000)
        else:
            l_pred = None

        _record(h_stats, h_pred, gt)
        if l_pred is not None:
            _record(l_stats, l_pred, gt)
            if h_pred == l_pred:
                agree += 1
        if len(examples) < 8:
            examples.append({
                "candidate": cc, "neighbor": nc, "age_d": age, "sim": sim,
                "ground_truth": gt, "heuristic": h_pred, "llm": l_pred,
            })

    out["expire"] = {
        "n": len(cases_expire),
        "heuristic": {
            **{"accuracy": round(h_stats.accuracy, 3),
               "fpr": round(h_stats.fpr, 3),
               "fnr": round(h_stats.fnr, 3)},
            **asdict(h_stats),
            "latency": _summary(h_lat),
        },
        "llm": (
            {**{"accuracy": round(l_stats.accuracy, 3),
                "fpr": round(l_stats.fpr, 3),
                "fnr": round(l_stats.fnr, 3)},
             **asdict(l_stats),
             "latency": _summary(l_lat)}
            if llm_call is not None else None
        ),
        "agreement_rate": (
            round(agree / len(cases_expire), 3)
            if llm_call is not None and cases_expire else None
        ),
        "examples": examples,
    }

    # ---- ARCHIVE ----
    h_stats = BinaryStats()
    l_stats = BinaryStats()
    agree = 0
    h_lat = []
    l_lat = []
    examples = []

    for (content, age, usage, score, mtype, gt) in cases_archive:
        nb = _make_neighbor(content, mtype, age, 0.0, usage=usage, score=score)
        t0 = time.perf_counter()
        h_pred, _h_reason = should_archive(nb)
        h_lat.append((time.perf_counter() - t0) * 1000)

        if llm_call is not None:
            t0 = time.perf_counter()
            l_pred, _l_raw = _llm_judge_archive(
                content, age, usage, score, mtype, llm_call=llm_call,
            )
            l_lat.append((time.perf_counter() - t0) * 1000)
        else:
            l_pred = None

        _record(h_stats, h_pred, gt)
        if l_pred is not None:
            _record(l_stats, l_pred, gt)
            if h_pred == l_pred:
                agree += 1
        if len(examples) < 8:
            examples.append({
                "content": content, "age_d": age, "usage": usage, "score": score,
                "type": mtype, "ground_truth": gt, "heuristic": h_pred, "llm": l_pred,
            })

    out["archive"] = {
        "n": len(cases_archive),
        "heuristic": {
            **{"accuracy": round(h_stats.accuracy, 3),
               "fpr": round(h_stats.fpr, 3),
               "fnr": round(h_stats.fnr, 3)},
            **asdict(h_stats),
            "latency": _summary(h_lat),
        },
        "llm": (
            {**{"accuracy": round(l_stats.accuracy, 3),
                "fpr": round(l_stats.fpr, 3),
                "fnr": round(l_stats.fnr, 3)},
             **asdict(l_stats),
             "latency": _summary(l_lat)}
            if llm_call is not None else None
        ),
        "agreement_rate": (
            round(agree / len(cases_archive), 3)
            if llm_call is not None and cases_archive else None
        ),
        "examples": examples,
    }

    return out


