def _heuristic_consolidate(cand_content: str, cand_type: str,
                            neighbors: list[tuple[str, str, float]]) -> str:
    """Reproduce the heuristic curator decision tree without DB plumbing.

    Mirrors `_heuristic_decide_memory_action` exactly:
      - empty content → IGNORE
      - noise filter → REJECT (we coalesce REJECT→IGNORE in the bench)
      - exact match same type → UPDATE
      - exact match cross type → MERGE
      - sim ≥ 0.85 same type → MERGE
      - sim ≥ 0.85 different type → CONTRADICTION
      - else ADD
    """
    from memoirs.core.normalize import should_skip_extraction

    if not cand_content.strip():
        return "IGNORE"
    skip, _ = should_skip_extraction(cand_content)
    if skip:
        return "IGNORE"  # coalesce REJECT into IGNORE for ground-truth label

    cand_norm = cand_content.strip().lower()
    for nc, nt, sim in neighbors:
        if nc.strip().lower() == cand_norm:
            return "UPDATE" if nt == cand_type else "MERGE"

    SEM_THRESH = 0.85
    for nc, nt, sim in neighbors:
        if sim >= SEM_THRESH:
            return "MERGE" if nt == cand_type else "CONTRADICTION"

    return "ADD"


def _llm_consolidate_judge(cand_content: str, cand_type: str,
                            neighbors: list[tuple[str, str, float]],
                            *, llm_call: Callable[[str], str]) -> str:
    if not cand_content.strip():
        return "IGNORE"
    nb_block = (
        "\n".join(f"  - [{nt}] {nc} (sim={sim:.2f})" for nc, nt, sim in neighbors)
        if neighbors else "  (none)"
    )
    prompt = (
        "Decide what to do with a new memoria candidate against existing "
        "neighbors. Reply with ONE of: ADD, UPDATE, MERGE, IGNORE, CONTRADICTION.\n"
        f"Candidate: [{cand_type}] {cand_content}\n"
        "Neighbors:\n"
        f"{nb_block}\n"
        "Decision:"
    )
    raw = llm_call(prompt)
    head = raw.strip().splitlines()[0].strip().upper() if raw.strip() else ""
    for tok in ("UPDATE", "MERGE", "CONTRADICTION", "IGNORE", "ADD"):
        if tok in head:
            return tok
    return "ADD"


def run_layer_b(cases: list, *, llm_call: Callable[[str], str] | None) -> dict:
    h_correct = 0
    l_correct = 0
    agree = 0
    confusion_h: dict = {}
    confusion_l: dict = {}
    h_lat: list[float] = []
    l_lat: list[float] = []
    examples: list[dict] = []

    for (cc, ct, nbs, gt) in cases:
        t0 = time.perf_counter()
        h_pred = _heuristic_consolidate(cc, ct, nbs)
        h_lat.append((time.perf_counter() - t0) * 1000)

        if llm_call is not None:
            t0 = time.perf_counter()
            l_pred = _llm_consolidate_judge(cc, ct, nbs, llm_call=llm_call)
            l_lat.append((time.perf_counter() - t0) * 1000)
        else:
            l_pred = None

        if h_pred == gt:
            h_correct += 1
        confusion_h.setdefault(gt, {}).setdefault(h_pred, 0)
        confusion_h[gt][h_pred] += 1
        if l_pred is not None:
            if l_pred == gt:
                l_correct += 1
            confusion_l.setdefault(gt, {}).setdefault(l_pred, 0)
            confusion_l[gt][l_pred] += 1
            if h_pred == l_pred:
                agree += 1

        if len(examples) < 10:
            examples.append({
                "candidate": cc, "type": ct, "n_neighbors": len(nbs),
                "ground_truth": gt, "heuristic": h_pred, "llm": l_pred,
            })

    n = len(cases)
    return {
        "n": n,
        "heuristic": {
            "accuracy": round(h_correct / n, 3) if n else 0.0,
            "correct": h_correct,
            "confusion": confusion_h,
            "latency": _summary(h_lat),
        },
        "llm": (
            {"accuracy": round(l_correct / n, 3) if n else 0.0,
             "correct": l_correct,
             "confusion": confusion_l,
             "latency": _summary(l_lat)}
            if llm_call is not None else None
        ),
        "agreement_rate": (
            round(agree / n, 3) if llm_call is not None and n else None
        ),
        "examples": examples,
    }


