"""GDPR-compliant data export and portable import (P3-6).

This module produces a self-describing zip bundle of the entire memoirs
corpus so users can take their data elsewhere (right of portability) or
move it between machines. The reverse operation re-hydrates a fresh DB
from a previously emitted bundle.

Bundle layout
-------------

    bundle.zip
    ├── manifest.json              schema_version, timestamp, counts, sha256
    ├── README.md                  human-friendly description (autogenerated)
    ├── memories.jsonl             one memory row per line (full columns)
    ├── conversations/
    │   └── <conv_id>.jsonl        messages + conversation header per line
    ├── embeddings.npz             np.savez(memory_id=…, vector=…) (optional)
    ├── entities.jsonl
    ├── relationships.jsonl
    ├── memory_links.jsonl
    ├── summary_nodes.jsonl
    └── provenance.jsonl           memory_id → source/conversation/messages

Notes
-----
- ``user_id`` is honoured if the column exists on the ``memories`` table
  (a future migration may introduce it). When the column is absent — the
  current default — the filter is silently ignored and we export the
  whole corpus, as instructed by the GAP P3-6 task.
- ``schema_version`` in the manifest is the ``PRAGMA user_version`` of
  the source DB; the importer uses it to detect mismatches.
- All file content is hashed with sha256 *before* zipping so
  :func:`verify_bundle` can detect tampering without unpacking the data
  itself.
"""
