def _load_jsonl(zf: zipfile.ZipFile, name: str) -> list[dict[str, Any]]:
    try:
        raw = zf.read(name)
    except KeyError:
        return []
    rows: list[dict[str, Any]] = []
    for line in raw.decode("utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        rows.append(json.loads(line))
    return rows


def _insert_row(
    conn: sqlite3.Connection,
    table: str,
    row: dict[str, Any],
    *,
    skip_columns: set[str] | None = None,
) -> bool:
    """Insert ``row`` into ``table`` using only columns that exist on the
    target schema. Returns True if the row was inserted (False on integrity
    error / duplicate).
    """
    skip = skip_columns or set()
    available = set(_column_names(conn, table))
    cols = [c for c in row.keys() if c in available and c not in skip]
    if not cols:
        return False
    placeholders = ", ".join(["?"] * len(cols))
    cols_csv = ", ".join(cols)
    sql = f"INSERT OR IGNORE INTO {table} ({cols_csv}) VALUES ({placeholders})"
    try:
        cur = conn.execute(sql, [row.get(c) for c in cols])
    except sqlite3.IntegrityError:
        return False
    return cur.rowcount > 0


def _delete_user_rows(conn: sqlite3.Connection, user_id: str | None) -> dict[str, int]:
    """Delete every row attributable to ``user_id`` from the live DB.

    With the current default schema (no ``user_id`` column), 'replace' wipes
    every row in the affected tables; that's the only safe interpretation
    when the DB cannot tell rows apart by user.
    """
    deleted: dict[str, int] = {}
    cur = conn.cursor()
    if user_id and _has_column(conn, "memories", "user_id"):
        for table, sql in (
            ("memory_embeddings",
             "DELETE FROM memory_embeddings WHERE memory_id IN "
             "(SELECT id FROM memories WHERE user_id = ?)"),
            ("memory_entities",
             "DELETE FROM memory_entities WHERE memory_id IN "
             "(SELECT id FROM memories WHERE user_id = ?)"),
            ("memory_links",
             "DELETE FROM memory_links WHERE source_memory_id IN "
             "(SELECT id FROM memories WHERE user_id = ?) "
             "OR target_memory_id IN (SELECT id FROM memories WHERE user_id = ?)"),
            ("memories", "DELETE FROM memories WHERE user_id = ?"),
        ):
            if not _has_table(conn, table):
                continue
            params = (user_id, user_id) if "OR target_memory_id" in sql else (user_id,)
            cur.execute(sql, params)
            deleted[table] = cur.rowcount
        return deleted
    # No user column → wipe wholesale.
    for table in (
        "memory_links", "memory_entities", "memory_embeddings",
        "summary_node_members", "summary_nodes",
        "relationships", "entities",
        "memories",
        "messages", "conversations", "sources",
    ):
        if not _has_table(conn, table):
            continue
        cur.execute(f"DELETE FROM {table}")
        deleted[table] = cur.rowcount
    return deleted


def import_user_data(
    db: MemoirsDB,
    *,
    in_path: Path,
    mode: ImportMode = "merge",
    new_user_id: Optional[str] = None,
    verify: bool = True,
) -> ImportReport:
    """Re-hydrate a memoirs DB from a bundle previously emitted by
    :func:`export_user_data`.

    Modes
    -----
    - ``merge``     — ``INSERT OR IGNORE`` every row, preserving existing
                       data. Duplicates are silently skipped.
    - ``replace``   — first delete all rows attributable to the bundle's
                       ``user_id`` (or wipe everything when no user column),
                       then insert.
    - ``new_user``  — assign every imported memory to ``new_user_id`` (only
                       meaningful when the schema has a ``user_id`` column).
                       Original ids are preserved; FKs are remapped where the
                       bundle relies on them.
    """
    in_path = Path(in_path)
    if mode not in ("merge", "replace", "new_user"):
        raise ValueError(f"unknown import mode: {mode!r}")
    if mode == "new_user" and not new_user_id:
        raise ValueError("mode='new_user' requires new_user_id")

    if verify:
        verdict = verify_bundle(in_path)
        if not verdict["ok"]:
            raise ValueError(
                f"bundle verification failed: "
                f"missing={verdict['missing']} mismatches={verdict['mismatches']}"
            )

    conn = db.conn
    has_user_col = _has_column(conn, "memories", "user_id")

    with zipfile.ZipFile(in_path, "r") as zf:
        manifest = json.loads(zf.read("manifest.json").decode("utf-8"))
        report = ImportReport(
            schema_version=int(manifest.get("schema_version", 0)),
            db_user_version=int(manifest.get("db_user_version", 0)),
            mode=mode,
            user_id_target=new_user_id if mode == "new_user" else manifest.get("user_id"),
        )

        # ---- replace: pre-delete --------------------------------------
        if mode == "replace":
            report.deleted = _delete_user_rows(conn, manifest.get("user_id"))

        # ---- sources ---------------------------------------------------
        # Sources have an INTEGER PRIMARY KEY AUTOINCREMENT; we map the
        # original source id to whatever id the destination assigns. The
        # uri column is UNIQUE so we use it as the natural key.
        src_id_remap: dict[int, int] = {}
        sources_in = _load_jsonl(zf, "sources.jsonl")
        skipped_src = 0
        inserted_src = 0
        for s in sources_in:
            existing = conn.execute(
                "SELECT id FROM sources WHERE uri = ?", (s["uri"],)
            ).fetchone()
            if existing:
                src_id_remap[int(s["id"])] = int(existing[0])
                skipped_src += 1
                continue
            cols = [c for c in s.keys() if c in set(_column_names(conn, "sources")) and c != "id"]
            placeholders = ", ".join(["?"] * len(cols))
            cur = conn.execute(
                f"INSERT INTO sources ({', '.join(cols)}) VALUES ({placeholders})",
                [s.get(c) for c in cols],
            )
            new_id = int(cur.lastrowid)
            src_id_remap[int(s["id"])] = new_id
            inserted_src += 1
        report.inserted["sources"] = inserted_src
        report.skipped["sources"] = skipped_src

        # ---- conversations + messages ---------------------------------
        # Walk every conversations/<id>.jsonl entry. The first line is the
        # conversation header, the rest are messages.
        conv_inserted = msg_inserted = conv_skipped = msg_skipped = 0
        for name in zf.namelist():
            if not name.startswith("conversations/") or not name.endswith(".jsonl"):
                continue
            entries = _load_jsonl(zf, name)
            header: dict[str, Any] | None = None
            messages: list[dict[str, Any]] = []
            for e in entries:
                if e.get("__type__") == "conversation":
                    header = e
                elif e.get("__type__") == "message":
                    messages.append(e)
            if header is None:
                continue
            # Remap source_id via src_id_remap
            orig_sid = header.get("source_id")
            if orig_sid is not None and int(orig_sid) in src_id_remap:
                header["source_id"] = src_id_remap[int(orig_sid)]
            cleaned = {k: v for k, v in header.items() if k != "__type__"}
            if _insert_row(conn, "conversations", cleaned):
                conv_inserted += 1
            else:
                conv_skipped += 1
            for m in messages:
                cleaned = {k: v for k, v in m.items() if k != "__type__"}
                if _insert_row(conn, "messages", cleaned):
                    msg_inserted += 1
                else:
                    msg_skipped += 1
        report.inserted["conversations"] = conv_inserted
        report.inserted["messages"] = msg_inserted
        report.skipped["conversations"] = conv_skipped
        report.skipped["messages"] = msg_skipped

        # ---- memories --------------------------------------------------
        memories = _load_jsonl(zf, "memories.jsonl")
        m_inserted = m_skipped = 0
        for m in memories:
            if mode == "new_user" and has_user_col:
                m = {**m, "user_id": new_user_id}
            if _insert_row(conn, "memories", m):
                m_inserted += 1
            else:
                m_skipped += 1
        report.inserted["memories"] = m_inserted
        report.skipped["memories"] = m_skipped

        # ---- entities + memory_entities + relationships --------------
        for table in ("entities", "memory_entities", "relationships"):
            rows = _load_jsonl(zf, f"{table}.jsonl")
            inserted = skipped = 0
            for r in rows:
                if _insert_row(conn, table, r):
                    inserted += 1
                else:
                    skipped += 1
            report.inserted[table] = inserted
            report.skipped[table] = skipped

        # ---- memory_links + summary tree ----------------------------
        for table in ("memory_links", "summary_nodes", "summary_node_members"):
            if not _has_table(conn, table):
                continue
            rows = _load_jsonl(zf, f"{table}.jsonl")
            inserted = skipped = 0
            for r in rows:
                if _insert_row(conn, table, r):
                    inserted += 1
                else:
                    skipped += 1
            report.inserted[table] = inserted
            report.skipped[table] = skipped

        # ---- embeddings ------------------------------------------------
        try:
            blob = zf.read("embeddings.npz")
        except KeyError:
            blob = b""
        emb_inserted = emb_skipped = 0
        if blob:
            try:
                emb_rows = _npz_to_embeddings(blob)
            except Exception as exc:
                report.warnings.append(f"embeddings.npz unreadable: {exc}")
                emb_rows = []
            for r in emb_rows:
                cols = ["memory_id", "dim", "embedding", "model", "created_at"]
                try:
                    cur = conn.execute(
                        "INSERT OR IGNORE INTO memory_embeddings "
                        "(memory_id, dim, embedding, model, created_at) "
                        "VALUES (?, ?, ?, ?, ?)",
                        (r["memory_id"], r["dim"], r["embedding"], r["model"], r["created_at"]),
                    )
                    if cur.rowcount > 0:
                        emb_inserted += 1
                    else:
                        emb_skipped += 1
                except sqlite3.IntegrityError:
                    emb_skipped += 1
        report.inserted["embeddings"] = emb_inserted
        report.skipped["embeddings"] = emb_skipped

        conn.commit()

    return report


