def anonymize_memory(memory: dict[str, Any]) -> dict[str, Any]:
    """Apply :func:`redact` to every string-typed value of ``memory``.

    Wrapper kept lightweight — does not touch JSON-encoded blobs by design;
    callers wanting deeper redaction can post-process metadata_json themselves.
    """
    out = dict(memory)
    for key, value in list(out.items()):
        if isinstance(value, str) and value:
            try:
                out[key] = redact(value).text
            except Exception:  # pragma: no cover — defensive
                pass
    return out


def _redact_message(msg: dict[str, Any]) -> dict[str, Any]:
    out = dict(msg)
    for key in ("content", "raw_json", "metadata_json"):
        val = out.get(key)
        if isinstance(val, str) and val:
            try:
                out[key] = redact(val).text
            except Exception:  # pragma: no cover
                pass
    return out


# ---------------------------------------------------------------------------
# Hash helper for the manifest
# ---------------------------------------------------------------------------


def _sha256_bytes(data: bytes) -> str:
    h = hashlib.sha256()
    h.update(data)
    return h.hexdigest()


# ---------------------------------------------------------------------------
# JSONL writer (in-memory, returns bytes for the zip)
# ---------------------------------------------------------------------------


def _jsonl_bytes(rows: Iterable[dict[str, Any]]) -> bytes:
    buf = io.StringIO()
    for r in rows:
        buf.write(json.dumps(r, ensure_ascii=False, sort_keys=True))
        buf.write("\n")
    return buf.getvalue().encode("utf-8")


# ---------------------------------------------------------------------------
# Embedding pack helpers (mirrors engine.embeddings._unpack semantics)
# ---------------------------------------------------------------------------


def _embeddings_to_npz(rows: list[dict[str, Any]]) -> bytes:
    """Pack memory embeddings into a numpy .npz blob.

    Each row is ``{memory_id, dim, embedding (BLOB), model, created_at}``.
    The .npz contains two parallel arrays: ``memory_ids`` (object) and
    ``vectors`` (float32, shape (N, dim)). When dimensions vary we store
    a list-of-arrays via numpy object dtype.
    """
    import numpy as np  # local: only loaded when bundling embeddings

    if not rows:
        # Empty placeholder still usable for round-trip; np.savez with no
        # arrays writes an empty zip which the importer handles.
        buf = io.BytesIO()
        np.savez(buf, memory_ids=np.array([], dtype=object), vectors=np.array([]))
        return buf.getvalue()

    dims = {int(r["dim"]) for r in rows}
    ids = np.array([r["memory_id"] for r in rows], dtype=object)
    models = np.array([r.get("model") or "" for r in rows], dtype=object)
    created = np.array([r.get("created_at") or "" for r in rows], dtype=object)

    if len(dims) == 1:
        dim = next(iter(dims))
        vectors = np.zeros((len(rows), dim), dtype=np.float32)
        for i, r in enumerate(rows):
            blob = r["embedding"] or b""
            if len(blob) != dim * 4:
                raise ValueError(
                    f"embedding for {r['memory_id']} has wrong size "
                    f"({len(blob)} bytes, expected {dim * 4})"
                )
            vectors[i] = np.frombuffer(blob, dtype=np.float32)
        buf = io.BytesIO()
        np.savez(
            buf,
            memory_ids=ids,
            vectors=vectors,
            models=models,
            created_at=created,
            dim=np.array([dim], dtype=np.int64),
        )
        return buf.getvalue()

    # Mixed-dim corner case (shouldn't really happen): store as object array.
    vec_objs = np.empty(len(rows), dtype=object)
    dim_arr = np.zeros(len(rows), dtype=np.int64)
    for i, r in enumerate(rows):
        blob = r["embedding"] or b""
        d = int(r["dim"])
        dim_arr[i] = d
        vec_objs[i] = np.frombuffer(blob, dtype=np.float32).copy()
    buf = io.BytesIO()
    np.savez(
        buf,
        memory_ids=ids,
        vectors=vec_objs,
        models=models,
        created_at=created,
        dim=dim_arr,
    )
    return buf.getvalue()


def _npz_to_embeddings(blob: bytes) -> list[dict[str, Any]]:
    """Inverse of :func:`_embeddings_to_npz`."""
    import numpy as np

    buf = io.BytesIO(blob)
    npz = np.load(buf, allow_pickle=True)
    ids = npz.get("memory_ids")
    if ids is None or len(ids) == 0:
        return []
    vectors = npz["vectors"]
    models = npz.get("models")
    created = npz.get("created_at")
    dim_arr = npz.get("dim")

    out: list[dict[str, Any]] = []
    if vectors.dtype == object:
        # mixed dim path
        for i, mid in enumerate(ids):
            v = np.asarray(vectors[i], dtype=np.float32)
            d = int(dim_arr[i]) if dim_arr is not None else int(v.shape[0])
            out.append({
                "memory_id": str(mid),
                "dim": d,
                "embedding": v.tobytes(),
                "model": str(models[i]) if models is not None and i < len(models) else "",
                "created_at": str(created[i]) if created is not None and i < len(created) else "",
            })
    else:
        d = int(vectors.shape[1]) if vectors.ndim == 2 else int(dim_arr[0]) if dim_arr is not None else 0
        for i, mid in enumerate(ids):
            v = np.asarray(vectors[i], dtype=np.float32)
            out.append({
                "memory_id": str(mid),
                "dim": d,
                "embedding": v.tobytes(),
                "model": str(models[i]) if models is not None and i < len(models) else "",
                "created_at": str(created[i]) if created is not None and i < len(created) else "",
            })
    return out


