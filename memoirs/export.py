_README_TEMPLATE = """# Memoirs export bundle

This zip is a portable snapshot of a memoirs corpus, produced by
``memoirs export``. It conforms to bundle schema_version={schema_version}
and was emitted at {timestamp} (UTC).

## Contents

- ``manifest.json`` — counts + sha256 hash for every other file in this bundle.
- ``memories.jsonl`` — one memory per line, full row.
- ``conversations/<conv_id>.jsonl`` — conversation header (ordinal=-1) followed
  by every active message in ordinal order.
- ``entities.jsonl``, ``relationships.jsonl`` — knowledge graph (Layer 3).
- ``memory_links.jsonl`` — A-MEM Zettelkasten edges (P1-3).
- ``summary_nodes.jsonl`` — RAPTOR summary tree (P1-6) when present.
- ``embeddings.npz`` — parallel arrays ``memory_ids`` / ``vectors``
  (float32). Absent when ``--no-embeddings`` was passed.
- ``provenance.jsonl`` — memory_id → source URI / conversation / messages.

## Re-import

    memoirs import path/to/this.zip --mode merge      # add, skip duplicates
    memoirs import path/to/this.zip --mode replace    # wipe + restore
    memoirs import path/to/this.zip --mode new_user --new-user-id alice

## Verify

    memoirs export verify path/to/this.zip
"""


def _render_readme(schema_version: int, timestamp: str) -> str:
    return _README_TEMPLATE.format(schema_version=schema_version, timestamp=timestamp)


