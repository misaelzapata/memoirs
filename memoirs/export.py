def verify_bundle(in_path: Path) -> dict[str, Any]:
    """Hash-check every payload file against the manifest. No DB writes.

    Returns ``{ok: bool, mismatches: [...], missing: [...], manifest: {...}}``.
    Never raises on a tampered bundle — instead reports the discrepancy so
    callers can decide whether to abort.
    """
    in_path = Path(in_path)
    if not zipfile.is_zipfile(in_path):
        return {"ok": False, "error": "not a zip", "manifest": None,
                "mismatches": [], "missing": []}
    with zipfile.ZipFile(in_path, "r") as zf:
        try:
            manifest = json.loads(zf.read("manifest.json").decode("utf-8"))
        except KeyError:
            return {"ok": False, "error": "manifest.json missing",
                    "manifest": None, "mismatches": [], "missing": []}
        files_meta = manifest.get("files", {})
        names = set(zf.namelist())
        mismatches: list[str] = []
        missing: list[str] = []
        for name, meta in files_meta.items():
            if name not in names:
                missing.append(name)
                continue
            data = zf.read(name)
            if _sha256_bytes(data) != meta.get("sha256"):
                mismatches.append(name)
            if int(meta.get("size", -1)) >= 0 and len(data) != meta["size"]:
                mismatches.append(name + " (size)")

    ok = not mismatches and not missing
    return {
        "ok": ok,
        "mismatches": sorted(set(mismatches)),
        "missing": sorted(set(missing)),
        "manifest": manifest,
    }


