def _filter_by_user_id(
    conn: sqlite3.Connection, user_id: Optional[str]
) -> tuple[str, tuple[Any, ...]]:
    """Return (where_clause, params) honoring the ``user_id`` filter.

    Falls back to ``("", ())`` when the column does not exist (current default
    schema) or when ``user_id`` is None — exporting the whole corpus.
    """
    if user_id is None:
        return "", ()
    if not _has_column(conn, "memories", "user_id"):
        log.info("memories.user_id column not present — exporting whole corpus")
        return "", ()
    return " WHERE user_id = ?", (user_id,)


def export_user_data(
    db: MemoirsDB,
    *,
    user_id: Optional[str] = "local",
    out_path: Path,
    include_embeddings: bool = True,
    redact_pii: bool = False,
    compression: int = zipfile.ZIP_DEFLATED,
) -> ExportManifest:
    """Export the memoirs corpus to ``out_path`` (a zip file).

    Parameters
    ----------
    db
        Open :class:`MemoirsDB`.
    user_id
        When the ``memories`` table has a ``user_id`` column, only rows
        matching this id are exported. Pass ``None`` to ignore the filter
        explicitly. With the current default schema the column is absent,
        so the whole corpus is dumped regardless of this argument.
    out_path
        Destination zip file. Parent directories are created on demand.
    include_embeddings
        When False, ``embeddings.npz`` is omitted from the bundle.
    redact_pii
        When True, every textual field is passed through
        :func:`memoirs.core.redact.redact` before serialization. Hashes,
        ids, and timestamps are left intact.
    compression
        ``zipfile.ZIP_DEFLATED`` (default) or ``zipfile.ZIP_STORED``.
    """
    out_path = Path(out_path)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    conn = db.conn
    where_mem, mem_params = _filter_by_user_id(conn, user_id)

    timestamp = datetime.now(timezone.utc).isoformat(timespec="seconds")
    files: dict[str, bytes] = {}
    counts: dict[str, int] = {}

    # ---- memories ---------------------------------------------------------
    mem_cols = _column_names(conn, "memories")
    mem_rows = [
        _row_to_dict(r)
        for r in conn.execute(f"SELECT * FROM memories{where_mem}", mem_params).fetchall()
    ]
    if redact_pii:
        mem_rows = [anonymize_memory(m) for m in mem_rows]
    files["memories.jsonl"] = _jsonl_bytes(mem_rows)
    counts["memories"] = len(mem_rows)

    memory_ids = {m["id"] for m in mem_rows}

    # ---- conversations + messages ----------------------------------------
    conv_rows = [_row_to_dict(r) for r in conn.execute("SELECT * FROM conversations").fetchall()]
    msg_rows = [
        _row_to_dict(r)
        for r in conn.execute(
            "SELECT * FROM messages ORDER BY conversation_id, ordinal"
        ).fetchall()
    ]
    if redact_pii:
        msg_rows = [_redact_message(m) for m in msg_rows]

    by_conv: dict[str, list[dict[str, Any]]] = {}
    for m in msg_rows:
        by_conv.setdefault(m["conversation_id"], []).append(m)

    counts["conversations"] = len(conv_rows)
    counts["messages"] = len(msg_rows)
    for conv in conv_rows:
        cid = conv["id"]
        # First line is the header (ordinal=-1 marker); the rest are messages
        # in ordinal order. The importer keys on ``conversation_id`` only.
        header = {"__type__": "conversation", **conv}
        msgs = by_conv.get(cid, [])
        body = [header] + [{"__type__": "message", **m} for m in msgs]
        files[f"conversations/{cid}.jsonl"] = _jsonl_bytes(body)

    # ---- sources (referenced by FK from conversations) -------------------
    src_rows = [_row_to_dict(r) for r in conn.execute("SELECT * FROM sources").fetchall()]
    files["sources.jsonl"] = _jsonl_bytes(src_rows)
    counts["sources"] = len(src_rows)

    # ---- embeddings -------------------------------------------------------
    if include_embeddings:
        emb_rows = [
            _row_to_dict(r)
            for r in conn.execute(
                "SELECT memory_id, dim, embedding, model, created_at "
                "FROM memory_embeddings"
            ).fetchall()
        ]
        # Filter to exported memories only.
        emb_rows = [r for r in emb_rows if r["memory_id"] in memory_ids]
        files["embeddings.npz"] = _embeddings_to_npz(emb_rows)
        counts["embeddings"] = len(emb_rows)
    else:
        counts["embeddings"] = 0

    # ---- entities + memory_entities + relationships ---------------------
    ent_rows = [_row_to_dict(r) for r in conn.execute("SELECT * FROM entities").fetchall()]
    files["entities.jsonl"] = _jsonl_bytes(ent_rows)
    counts["entities"] = len(ent_rows)

    me_rows = [
        _row_to_dict(r) for r in conn.execute("SELECT * FROM memory_entities").fetchall()
    ]
    files["memory_entities.jsonl"] = _jsonl_bytes(me_rows)
    counts["memory_entities"] = len(me_rows)

    rel_rows = [
        _row_to_dict(r) for r in conn.execute("SELECT * FROM relationships").fetchall()
    ]
    files["relationships.jsonl"] = _jsonl_bytes(rel_rows)
    counts["relationships"] = len(rel_rows)

    # ---- memory_links (P1-3) — table exists from migration 002 ----------
    if _has_table(conn, "memory_links"):
        link_rows = [
            _row_to_dict(r)
            for r in conn.execute(
                "SELECT source_memory_id, target_memory_id, similarity, reason, created_at "
                "FROM memory_links"
            ).fetchall()
        ]
        files["memory_links.jsonl"] = _jsonl_bytes(link_rows)
        counts["memory_links"] = len(link_rows)
    else:
        files["memory_links.jsonl"] = b""
        counts["memory_links"] = 0

    # ---- summary nodes (P1-6) ------------------------------------------
    if _has_table(conn, "summary_nodes"):
        sn_rows = [
            _row_to_dict(r)
            for r in conn.execute(
                "SELECT id, level, content, child_count, created_at, parent_id, "
                "scope_kind, scope_id FROM summary_nodes"
            ).fetchall()
        ]
        files["summary_nodes.jsonl"] = _jsonl_bytes(sn_rows)
        counts["summary_nodes"] = len(sn_rows)
        if _has_table(conn, "summary_node_members"):
            mem_link_rows = [
                _row_to_dict(r)
                for r in conn.execute("SELECT * FROM summary_node_members").fetchall()
            ]
            files["summary_node_members.jsonl"] = _jsonl_bytes(mem_link_rows)
            counts["summary_node_members"] = len(mem_link_rows)
    else:
        files["summary_nodes.jsonl"] = b""
        counts["summary_nodes"] = 0

    # ---- provenance: memory_id → source/conversation/messages ----------
    # We derive provenance from memory_candidates.source_message_ids when
    # available. ``source_message_ids`` is a JSON array of message ids.
    prov_rows: list[dict[str, Any]] = []
    if _has_table(conn, "memory_candidates"):
        cand_rows = conn.execute(
            "SELECT promoted_memory_id, conversation_id, source_message_ids "
            "FROM memory_candidates "
            "WHERE promoted_memory_id IS NOT NULL"
        ).fetchall()
        for r in cand_rows:
            mid = r["promoted_memory_id"]
            if mid not in memory_ids:
                continue
            try:
                msg_ids = json.loads(r["source_message_ids"] or "[]")
            except json.JSONDecodeError:
                msg_ids = []
            prov_rows.append({
                "memory_id": mid,
                "conversation_id": r["conversation_id"],
                "message_ids": msg_ids,
            })
    files["provenance.jsonl"] = _jsonl_bytes(prov_rows)
    counts["provenance"] = len(prov_rows)

    # ---- README + manifest -----------------------------------------------
    files["README.md"] = _render_readme(SCHEMA_VERSION, timestamp).encode("utf-8")

    file_metadata: dict[str, dict[str, Any]] = {}
    for name, data in files.items():
        file_metadata[name] = {"sha256": _sha256_bytes(data), "size": len(data)}

    manifest = ExportManifest(
        schema_version=SCHEMA_VERSION,
        db_user_version=current_version(conn),
        timestamp=timestamp,
        user_id=user_id,
        redact_pii=redact_pii,
        counts=counts,
        files=file_metadata,
        bundle_path=str(out_path.resolve()),
    )
    manifest_bytes = json.dumps(
        manifest.to_dict(), indent=2, sort_keys=True, ensure_ascii=False
    ).encode("utf-8")

    # ---- assemble zip ----------------------------------------------------
    with zipfile.ZipFile(out_path, "w", compression=compression) as zf:
        # manifest first so streaming readers can validate before unpacking.
        zf.writestr("manifest.json", manifest_bytes)
        for name, data in files.items():
            zf.writestr(name, data)

    return manifest


